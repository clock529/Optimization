clear;
clc;

x(:, 1) = [8, 8]';
r(1) = 1;
c = 0.1;
eps = 1e-10;
k = 1;
while (r(k) * (1/x(1,k) + 1/x(2,k) + 1/(20-(x(1,k)+ x(2,k))))) > eps
    r(k + 1) = c * r(k);
    x(:, k + 1) = PRP(x(:, k), r(k+1));
    
    k = k + 1;
end

